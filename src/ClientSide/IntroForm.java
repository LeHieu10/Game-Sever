    void connectServer() {

        try {
            if (socket == null) {
                socket = new Socket(IP, Integer.parseInt(Port));
                client = new Client(socket);
                client.receive = this;
                client.start();
            }
        } catch (IOException | NumberFormatException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Can't connect to Server!", "Error", 1);
        }
    }

