    @Override
    public void ReceiveMessage(Message msg) {
        switch (msg.getType()) {
            case 10: {
                user = (Users) msg.getObject();
                if (user != null) {
                    client.user = user;
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            System.out.println("CLIENT");
                            new RoomFrame(client).setVisible(true);
                        }
                    });
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Ten ton tai", "Message", 1);
                }
                break;
            }
        }
    }
